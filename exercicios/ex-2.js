let A = 10;
let B = 5;

// Adição
const soma = A + B;
console.log(`A soma de A e B é: ${soma}`);

// Subtração
const subtracao = A - B;
console.log(`A subtração de A e B é: ${subtracao}`);

// Multiplicação
const multiplicacao = A * B;
console.log(`A multiplicação de A e B é: ${multiplicacao}`);

// Divisão
const divisao = A / B;
console.log(`A divisão de A e B é: ${divisao}`);